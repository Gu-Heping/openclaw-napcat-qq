export function findFaceId(name: string): string | undefined {
  const alias = FACE_ALIASES[name];
  if (alias) return alias;

  for (const [id, faceName] of Object.entries(NTQQ_FACE_MAP)) {
    if (faceName === name) return id;
  }
  for (const [id, faceName] of Object.entries(NTQQ_FACE_MAP)) {
    if (faceName.includes(name) || name.includes(faceName)) return id;
  }
  return undefined;
}

export function searchFaces(keyword: string): Array<{ id: string; name: string }> {
  return Object.entries(NTQQ_FACE_MAP)
    .filter(([, name]) => name.includes(keyword))
    .map(([id, name]) => ({ id, name }));
}
